<p><strong>Livehealth</strong></p>
<p>The fitness start-up, Livehealth aims to bridge the gap between consumers and healthcare providers in the best way possible. It was founded in the year 2013 by Abhimanyu Bhosale (Co-founder & CEO) and Mukund Malani (Co-founder & CTO). They help patients, health providers, researchers and other government bodies, to analyze the vast medical data that is generated on a daily basis, competently. With revolutionizing healthcare delivery, it also helps providers manage their day to day processes better.
</p>
<p>Currently, Livehealth’s revenue model is B2B, charging diagnostic centres and laboratories for using software to perform day to day functions. The revenue collected by Livehealth is on a per month basis so a customer does not have to pay a huge amount as per the annual charges. The other revenue model used by Livehealth is B2C. The fitness start-up has more than 175 Diagnostic Centres and Laboratories as customers across 18 cities. It also boasts of 5,000 monthly active users as well as 1 million + generated medical records in a month.
</p>
<p><strong>Fitpass</strong></p>
<p>Fitpass was founded by Arushi Verma and Akshay Verma in the year 2015. This fitness start-up is your workout pass to across 1000+ gyms as well as fitness studios in Delhi NCR at anytime. Collecting revenue of Rs. 999 per month, the fitness start-up offers its users great flexibility to select any kind of work out being spoilt over choices like Zumba, Yoga, Pilates, MMA, Aerobics, circuit training, Cross Fit, spinning, kick boxing, boot camp, etc.  It is an app based fitness service that is available on iOS & Android.

</p>
<p>The fitness start-up offers 1-month fitness plan at the rate of Rs. 999 and another economic 3-month plan that is revenue at Rs. 2499. The Fitpass also offers six proprietary products like 6 Pack Abs, 36-24-36, Sexy Calves, Pecs of Steel, Dolay Sholay and Bikini Bod. All these fitness products are ranged between Rs. 499 to Rs. 999. 4000+ apps were downloaded in the last few weeks alone and week by week growth of the fitness start-up is 150-200%.
</p>
<p><strong>Superdoc</strong></p>
<p>Superdoc was founded by Chaitanya Mehta (GWU), Dr. Dinesh Rijhwani (KEM MBBS, IIM-A MBA), and Abhishek Mane (NYU) in the year 2015. The company allows users to get health advice from doctors for free. Doctors are bound to answer their patient quarries by 10 minutes after they view the reports and photos of the problem. The service provided in the app is live from 8am to 12am, round the year.
</p>
<p>The fitness start-up charges fees from the chats on a monthly basis. They hold doctor and psychologist chats on a more premium model with the first chat to a doctor being free.
